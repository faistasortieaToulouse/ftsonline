// Fonction pour récupérer le saint du jour
export function getSaintDuJour(date: Date): string {
  const month = String(date.getMonth() + 1).padStart(2, "0");
  const day = String(date.getDate()).padStart(2, "0");
  const key = `${month}-${day}`;

  // Si 29 février dans une année non bissextile, retourner le 28 février
  if (key === "02-29" && !(date.getFullYear() % 4 === 0 && (date.getFullYear() % 100 !== 0 || date.getFullYear() % 400 === 0))) {
    return saintsDuJour["02-28"];
  }

  return saintsDuJour[key] || "Saint inconnu";
}
